'''
Wap to find area of circle and radius is input by user
Area of circle = PI * radius * radius
'''
# input radius from user and store it in radius variable
radius = float(input("Enter the radius of the circle: "))

# define the value of PI
PI = 3.14

# calculate area of circle and store it in area variable
area = PI * radius ** 2

# display the area of circle
print("Area of circle  is : ", area)
